// создаем массив
var animals = ['Cat', 'Dog', 'Horse']
alert(animals)

// push() - доб. элемент в КОНЕЦ массива
animals.push('Fish')
alert(animals)

// unshift() - доб. элемент в НАЧАЛО массива
animals.unshift('Goose')
alert(animals)

// pop() - удаляет эл. из КОНЦА массива
animals.pop()
alert(animals)

// shift() - удаляет ел. из НАЧАЛА массива
animals.shift()
alert(animals)